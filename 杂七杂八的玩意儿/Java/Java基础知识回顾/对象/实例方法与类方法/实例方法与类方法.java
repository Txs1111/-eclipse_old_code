package Java基础知识回顾.对象.实例方法与类方法;

/**
 * 类方法也是方法，与方法本质相同，只是会一直存在在内存中罢了
 * 
 * @author Lyon
 *
 */
public class 实例方法与类方法 {
	public static void main(String[] args) {
		// 调用实例方法
		实例方法与类方法之实例方法 n;
		// 实例后才会为其分配内存
		n = new 实例方法与类方法之实例方法();
		// 必须使用实例的对象，才可以调用里面的方法
		n.实例方法();

		// 调用类方法
		// 声明时即会为其分配内存
		实例方法与类方法之类方法 m;
		// 可直接用类名调用其方法，！！！但是这个方法会一直在内存中，直到程序结束时才会被取消
		实例方法与类方法之类方法.类方法();
	}
}
