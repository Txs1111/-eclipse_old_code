package Java基础知识回顾.对象.访问权限;

import Java基础知识回顾.对象.访问权限引用测试包.友好变量与友好方法类引用的类其他包;

/**
 * 注意：如果源文件使用import语句引用了另一个包中的类，并用该类创建了一个对象，那么该类的这个对象将不能访问自己的友好变量和友好方法。
 * 即使可访问自己包下的类，其他的包下类里的变量与方法无法访问。
 * 
 * @author Lyon
 *
 */
public class 友好变量与友好方法 {
	public static void main(String[] args) {
		友好变量与友好方法类引用的类 n = new 友好变量与友好方法类引用的类();
		n.a = 18;
		友好变量与友好方法类引用的类其他包 m = new 友好变量与友好方法类引用的类其他包();
		// m.a;//报错，无法引用其他包中的变量与方法
	}
}
